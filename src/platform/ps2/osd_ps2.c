/* ── Color conversion ────────────────────────────────────────────── */

static inline uint16_t color32_to_16s(uint32_t c)
{
    uint8_t r = (c >> 0) & 0xFF;
    uint8_t g = (c >> 8) & 0xFF;
    uint8_t b = (c >> 16) & 0xFF;
    return (uint16_t)((r >> 3) | ((g >> 3) << 5) | ((b >> 3) << 10));
}

/* ── Upload one rendered text line to VRAM via IMAGE transfer ────── */

static void osd_upload_line(int vram_x, int vram_y,
                            const char *text, int nchars,
                            uint16_t fg, uint16_t bg)
{
    if (nchars <= 0) return;
    if (nchars > OSD_LINE_MAX) nchars = OSD_LINE_MAX;

    int pw = nchars * OSD_CHAR_W;
    int ph = OSD_CHAR_H;

    /* Clamp to PSX VRAM bounds */
    if (vram_x + pw > PSX_VRAM_WIDTH) pw = PSX_VRAM_WIDTH - vram_x;
    if (vram_y + ph > PSX_VRAM_HEIGHT) ph = PSX_VRAM_HEIGHT - vram_y;
    if (pw <= 0 || ph <= 0) return;

    /* Render font pixels into line buffer */
    for (int c = 0; c < nchars; c++)
    {
        unsigned char ch = text[c];
        int glyph = (ch >= 0x20 && ch <= 0x7E) ? (ch - 0x20) : 0;
        for (int py = 0; py < ph; py++)
        {
            uint8_t bits = font_8x8[glyph][py];
            for (int px = 0; px < OSD_CHAR_W; px++)
            {
                int idx = py * pw + c * OSD_CHAR_W + px;
                if (c * OSD_CHAR_W + px < pw)
                    osd_line_buf[idx] = (bits & (0x80 >> px)) ? fg : bg;
            }
        }
    }

    /* DCache sync before GIF IMAGE transfer */
    int nbytes = pw * ph * (int)sizeof(uint16_t);
    SyncDCache(osd_line_buf, (void *)((uintptr_t)osd_line_buf + nbytes));

    /* BITBLTBUF → TRXPOS → TRXREG → TRXDIR (A+D mode) */
    Push_GIF_Tag(GIF_TAG_LO(4, 0, 0, 0, 0, 1), GIF_REG_AD);
    Push_GIF_Data(GS_SET_BITBLTBUF(0, 0, 0, 0, PSX_VRAM_FBW, PSX_VRAM_PSM),
                  GS_REG_BITBLTBUF);
    Push_GIF_Data(GS_SET_TRXPOS(0, 0, vram_x, vram_y, 0), GS_REG_TRXPOS);
    Push_GIF_Data(GS_SET_TRXREG(pw, ph), GS_REG_TRXREG);
    Push_GIF_Data(GS_SET_TRXDIR(0), GS_REG_TRXDIR); /* host → local */

    /* IMAGE data: CT16S pixels, padded to QW boundary */
    int nqw = (nbytes + 15) / 16;
    Push_GIF_Tag(GIF_TAG_LO(nqw, 0, 0, 0, 2, 0), 0); /* FLG=2 IMAGE, EOP=0 */

    const gif_qword_t *src = (const gif_qword_t *)osd_line_buf;
    for (int i = 0; i < nqw; i++)
        Push_GIF_Data(src[i].d0, src[i].d1);
}

/* ── Render one text entry ───────────────────────────────────────── */

static void osd_draw_entry(const osd_entry_t *e)
{
    uint16_t fg = color32_to_16s(e->color);
    uint16_t bg = 0x0000; /* black background */

    const char *s = e->text;
    int vx = e->x;
    int vy = e->y;

    while (*s)
    {
        /* Find end of current line (newline or end of string) */
        const char *eol = s;
        int nchars = 0;
        while (*eol && *eol != '\n' && nchars < OSD_LINE_MAX)
        {
            nchars++;
            eol++;
        }

        if (nchars > 0)
            osd_upload_line(vx, vy, s, nchars, fg, bg);

        s = eol;
        if (*s == '\n') { s++; vy += OSD_CHAR_H; }
    }
}

/* ── Public API ──────────────────────────────────────────────────── */

static int boot_log_row = 0; /* next row for boot log (in VRAM pixel Y) */

void osd_init(void)
{
    boot_log_row = 0;
    osd_initialized = 1;
}

void osd_boot_log(const char *fmt, ...)
{
    if (!osd_initialized) return;

    char buf[OSD_MAX_TEXT];
    va_list ap;
    va_start(ap, fmt);
    vsnprintf(buf, sizeof(buf), fmt, ap);
    va_end(ap);

    /* Also print to console */
    printf("%s\n", buf);

    int len = (int)strlen(buf);
    if (len > OSD_LINE_MAX) len = OSD_LINE_MAX;
    if (len > 0)
    {
        uint16_t fg = color32_to_16s(OSD_COLOR_WHITE);
        uint16_t bg = 0x0000;
        osd_upload_line(0, boot_log_row, buf, len, fg, bg);
        Flush_GIF_Sync();
    }
    boot_log_row += OSD_CHAR_H;
    if (boot_log_row >= PSX_VRAM_HEIGHT)
        boot_log_row = 0; /* wrap */
}

void osd_printf(int x, int y, uint32_t color, const char *fmt, ...)
{
    if (osd_entry_count >= OSD_MAX_ENTRIES) return;
    osd_entry_t *e = &osd_entries[osd_entry_count];
    e->x = x;
    e->y = y;
    e->color = color;

    va_list ap;
    va_start(ap, fmt);
    vsnprintf(e->text, OSD_MAX_TEXT, fmt, ap);
    va_end(ap);

    osd_entry_count++;
}

void osd_clear(void)
{
    osd_entry_count = 0;
}

void osd_set_enabled(int enabled)
{
    osd_enabled = enabled;
}

int osd_is_enabled(void)
{
    return osd_enabled;
}

void osd_draw(void)
{
    if (!osd_enabled || osd_entry_count == 0 || !osd_initialized)
    {
        osd_entry_count = 0;
        return;
    }

    for (int i = 0; i < osd_entry_count; i++)
        osd_draw_entry(&osd_entries[i]);

    osd_entry_count = 0;
}
